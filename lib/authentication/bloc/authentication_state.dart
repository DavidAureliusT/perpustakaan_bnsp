part of 'authentication_bloc.dart';

class AuthenticationState extends Equatable {
  const AuthenticationState._({
    this.status = AuthenticationStatus.unknown,
    this.user = User.empty,
  });

  const AuthenticationState.unknown() : this._();

  const AuthenticationState.memberAuthenticated(User user)
      : this._(status: AuthenticationStatus.memberAuthenticated, user: user);

  const AuthenticationState.adminAuthenticated(User user)
      : this._(status: AuthenticationStatus.adminAuthenticated, user: user);

  const AuthenticationState.unauthenticated()
      : this._(status: AuthenticationStatus.unauthenticated);

  final AuthenticationStatus status;
  final User user;

  @override
  List<Object> get props => [status, user];
}
